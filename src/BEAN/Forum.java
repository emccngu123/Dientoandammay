package BEAN;

public class Forum {

	private int idForum;
	private String title;
	
	public Forum() {}
	

	public int getidForum() {
		return idForum;
	}

	public void setidForum(int id) {
		this.idForum = id;
	}


	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}


}

package BEAN;

public class UserForum {

	private int id;
	private int idForum;
	private String username;
	private String message;
	
	public UserForum() {}
	

	public int getID() {
		return id;
	}

	public void setID(int password) {
		this.id = password;
	}


	public int getidForum() {
		return idForum;
	}

	public void setidForum(int fullname) {
		this.idForum = fullname;
	}
	
	public String getUser() {
		return username;
	}

	public void setUser(String fullname) {
		this.username = fullname;
	}
	
	public String getMessage() {
		return message;
	}

	public void setMessage(String fullname) {
		this.message = fullname;
	}


}
